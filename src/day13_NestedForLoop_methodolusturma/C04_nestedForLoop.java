package day13_NestedForLoop_methodolusturma;

public class C04_nestedForLoop {
    public static void main(String[] args) {

        /*
        * * * * *
        * * * * *
        * * * * *
        * * * * *

         */

        for (int i = 1; i <=6 ; i++) {

            for (int j = 1; j <=5; j++) {

                System.out.print("* ");

            }
            System.out.println("");
        }







    }
}
