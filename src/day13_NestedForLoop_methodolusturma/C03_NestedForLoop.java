package day13_NestedForLoop_methodolusturma;

public class C03_NestedForLoop {
    public static void main(String[] args) {

       //1-4 arasindaki sayilar icin carpim tablosu olusturun


        for (int i = 1; i <=4 ; i++) {

            for (int j = 1; j <=4 ; j++) {

                System.out.print(i*j + "");

            }
            System.out.println(" ");
        }


    }
}
